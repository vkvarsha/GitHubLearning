package javaprogramme;

public class Continue_Keyword_71
{

	/*Continue keyword is used to skip the particular iteration */
	public static void main(String[] args) 
	
	{
		
		for(int i=0;i<6;i++)
		{
			if(i==4)
			{
			continue;
			}
			System.out.println(i);
			
			
			
		}
		
		
	}
	
	
	
	
	
	
}
