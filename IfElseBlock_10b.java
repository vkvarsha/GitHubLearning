package javaprogramme;

public class IfElseBlock_10b 
{
	/*  if else
	 * syntax
	 * if (condition)
	 * {
	 * 
	 * sysout(if output)
	 * 
	 * }
	 *  else 
	 *  {
	 *  
	 *  sysout(else output)
	 *  
	 *  }
	 *  
	 *  In if else block, if if condition is true then if output will be execute, 
	 *  				  if if condition is false then else output will be execute
	 *  
	 *  */
	
	
	
	public static void main(String[] args)
	{
	
		int age =10;
		
		if(age > 18)
		{
			System.out.println("you can start vote now");
			
		}
		else
		{
			System.out.println("You can not vote");
			
		}
	}

}
