package javaprogramme;

public class Ifblock_10a {

	public static void main(String[] args) {
		int age = 50;
		
		if(age>18)
		{
			System.out.println("You can Vote");
		}
		
		if(age<18)
		{
			System.out.println("You can not Vote");
		}
		
		if(age==18)
		{
			System.out.println("You can start Voting");
		}
				
		if(age!=18)
		{
			System.out.println("You can check other option to Vote");
		}
	}

}
